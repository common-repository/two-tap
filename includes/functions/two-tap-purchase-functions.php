/**
 * Get the Two Tap Purchase by the Two Tap purchase_id
 *
 * @param  [type] $purchase_id [description]
 *
 * @return [type]              [description]
 */
function get_twotap_purchase($purchase_id = null){
    if( is_null($purchase_id) ){
        return false;
    }

    $args = array(
        'post_type' => TT_POST_TYPE_PURCHASE,
        'post_status' => twotap_db_purchase_statuses(),
        'posts_per_page' => '-1',
        'order' => 'DESC',
        'orderby' => 'post_modified',
        'meta_query' => array(
            array(
                'key' => TT_META_TWOTAP_PURCHASE_ID,
                'value' => $purchase_id,
                'compare' => '=',
                )
            )
        );
    $query = new WP_Query($args);

    if($query->have_posts()){
        $present = $query->get_posts();
        $db_purchase = array_shift($present);
        return $db_purchase;
    } else {
        return false;
    }

}

